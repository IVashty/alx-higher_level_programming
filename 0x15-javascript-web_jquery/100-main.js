// adds an event listener which listens for the "DOMContentLoaded" event.
window.addEventListener('DOMContentLoaded', (event) => {
        // Get the header element by its tag name and assign it to the 'header' variable
        const header = document.querySelector('header');
        // Change the 'color' style property of the header element to red
        header.style.color = '#FF0000';
      });
